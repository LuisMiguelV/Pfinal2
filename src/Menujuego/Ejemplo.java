       double suma=0, promedio;
       int j;
       
       notas [0]= Double.parseDouble(Txt1.getText());
       notas [1]= Double.parseDouble(Txt2.getText()); 
       notas [2]= Double.parseDouble(Txt3.getText()); 
       
       for(j=0;j<notas.length;j++){
           suma= suma+notas[j];
       }
        promedio=suma/3;
        
        Notafinal.setText(String.valueOf(promedio));
        
