    public void presionar(int casilla){
        if(LB[casilla-1].getText().equals("") && Est){
        LB[casilla-1].setText(turno);
        CambiarT();
        Comproganador();
        }    
        
    }
     public void CambiarT(){
    
      if (turno.equals("X") )
      {
         turno = "O"  ;
           
      }
      else  {
       turno = "X" ;  
     }
      
     
      Nombre1.setText("Turno de: "+turno);
     }
     
    public void Comproganador(){
        for(int j=0; j<Vic.length;j++){
           if(LB[Vic[j][0]-1].getText().equals("X")  &&
              LB[Vic[j][1]-1].getText().equals("X") &&
              LB[Vic[j][2]-1].getText().equals("X")){
               
               
               LB[Vic[j][0]-1].setBackground(Color.orange);  
               LB[Vic[j][1]-1].setBackground(Color.orange);
               LB[Vic[j][2]-1].setBackground(Color.orange);
               Puntajex.setText(Integer.toString(Integer.parseInt(Puntajex.getText())+1));
               Nombre1.setText("Gano X ");
               Est=false;
               
           }
           if(LB[Vic[j][0]-1].getText().equals("O")  &&
                LB[Vic[j][1]-1].getText().equals("O") &&
                   LB[Vic[j][2]-1].getText().equals("O")){
               
               
               LB[Vic[j][0]-1].setBackground(Color.orange);  
               LB[Vic[j][1]-1].setBackground(Color.orange);
               LB[Vic[j][2]-1].setBackground(Color.orange); 
               
               Puntajeo.setText(Integer.toString(Integer.parseInt(Puntajeo.getText())+1));
               Nombre1.setText("Gano O "); 
               Est=false; 
           }
           
         
        }
    }
    
